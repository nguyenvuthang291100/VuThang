    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        Color mauxanh = new Color(255, 153, 0);
        btnThem.setBackground(mauxanh);
        btnSua.setEnabled(true);
        btnSua.setBackground(mauxanh);
        btnXoa.setEnabled(true);
        btnXoa.setBackground(mauxanh);
        btnLuu.setEnabled(false);
        btnLuu.setBackground(Color.GRAY);
        int chon = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Lưu?", "Lưu", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (chon == JOptionPane.YES_NO_OPTION) {
            if (KTDL()) {
                if (them) {
                    try {
                        Connection conn = dao.getConnect();
                        String caulenh = "INSERT INTO tbl_khachhang VALUE(Null,'" + txtTenkhachhang.getText() + "','" + cboLoai.getSelectedItem() + "')";
                        boolean a = dao.Lenh(caulenh, conn);
                        if (a) {
                            tblKhachhang.removeAll();
                            tablemodel.setRowCount(0);
                            loadnv();
                            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
                        } else {
                            JOptionPane.showMessageDialog(this, "Thêm không thành công");
                        }
                    } catch (Exception ex) {
                        Logger.getLogger(frmKH.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if (sua) {
                    try {
                        Connection conn = dao.getConnect();
                        String caulenh = "update tbl_khachhang set TenKH='" + txtTenkhachhang.getText() + "',Loai='" + cboLoai.getSelectedItem() + "'where MaKH=" + txtMakhachhang.getText();
                        boolean a = dao.Lenh(caulenh, conn);
                        if (a) {
                            tblKhachhang.removeAll();
                            tablemodel.setRowCount(0);
                            loadnv();
                            JOptionPane.showMessageDialog(this, "sửa Thành Công");
                        } else {
                            JOptionPane.showMessageDialog(this, "sửa không thành công");
                        }
                    } catch (Exception ex) {
                        Logger.getLogger(frmKH.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
    }//GEN-LAST:event_btnLuuActionPerformed
    }
    private void lbltikmkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lbltikmkiemActionPerformed
        // TODO add your handling code here:
        tblKhachhang.removeAll();
        tablemodel.setRowCount(0);
        String tk = txtTimkiem.getText();
        ArrayList<TblKhachhang> list = new ArrayList();
        list.clear();
        try {
            list = nvDL.timkiemKH(tk);
            tablemodel = (DefaultTableModel) tblKhachhang.getModel();
            for (TblKhachhang nv : list) {
                tablemodel.addRow(new Object[]{nv.getMaKH(), nv.getTenKH(), nv.getLoai()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
    }//GEN-LAST:event_lbltikmkiemActionPerformed
    void hienthi(int i) {
        TableModel model = tblKhachhang.getModel();
        txtMakhachhang.setText(model.getValueAt(i, 0).toString());
        txtTenkhachhang.setText(model.getValueAt(i, 1).toString());
        cboLoai.setSelectedItem(model.getValueAt(i, 2).toString());

    }

