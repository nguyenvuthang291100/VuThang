    void loadnv() {

        ArrayList<TblKhachhang> list = new ArrayList();
        list.clear();
        try {
            list = nvDL.getListKH();
            tablemodel = (DefaultTableModel) tblKhachhang.getModel();
            for (TblKhachhang nv : list) {
                tablemodel.addRow(new Object[]{nv.getMaKH(), nv.getTenKH(), nv.getLoai()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }

    }

    private boolean KTDL() throws HeadlessException {
        StringBuilder sb = new StringBuilder();

        if (txtTenkhachhang.getText().equals("")) {
            sb.append("Vui lòng nhập tên khách hàng\n");
            txtTenkhachhang.requestFocus();
        }

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }

