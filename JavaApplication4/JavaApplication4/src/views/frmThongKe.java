    void loadnv() {

        ArrayList<TblHoadonbanhang> list = new ArrayList();
        list.clear();
        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd", Locale.getDefault());
            String tungay = dateFormat.format(DateTuNgay.getDate());
            String denngay = dateFormat.format(DateDanNgay.getDate());
            list = BHDL.getThongKe(tungay, denngay);
            table = (DefaultTableModel) tblThongKe.getModel();
            double tongtien = 0;
            for (TblHoadonbanhang nv : list) {
                table.addRow(new Object[]{nv.getMaHDBH(), nv.getTenKH(), nv.getNgayLap(), nv.getTenNV(), nv.getTongTien()});
                tongtien += nv.getTongTien();
            }
            lblDoanhThu.setText(tongtien + " VND");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());

        }

    }

