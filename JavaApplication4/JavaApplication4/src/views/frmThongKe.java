 private boolean KTDL() throws HeadlessException {
        StringBuilder sb = new StringBuilder();
        if (txtMaHDBH.getText().equals("")) {
            sb.append("Vui lòng chọn hóa đơn trong bảng");
            txtMaHDBH.requestFocus();
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb.toString(), "Thông báo", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }
